DEFAULT_LOCALE = "ru"


def get_translator(locale: str):
    strings = LOCALES.get(locale, LOCALES[DEFAULT_LOCALE])

    def t(key: str, **kwargs) -> str:
        template = strings.get(key, key)
        return template.format(**kwargs) if kwargs else template

    return t
